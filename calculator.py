
import numpy as np
#creating two matrices for the matrix operations.
x= np.array([1,2,3], [4,5,6])
y=np.array([7,8,9], [3,4,5])
#performing operations on matrix x and y

print('addition of x and y matrix is :', np.add(x,y))
print('subtraction of x and y matrix is :', np.subtract(x,y))
print('division of x and y matrix is :', np.divide(x,y))